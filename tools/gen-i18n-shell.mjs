// Regenerates src/i18n/shell.ts — the small EAGER subset of UI strings.
//
// en.ts / zh-Hant.ts / batchB.ts hold ~570 kB of PER-MODULE strings that are only
// needed (a) when a module renders inside the lazy ModuleDetail chunk, or (b) when
// the command palette runs a feature search. Importing them eagerly dragged all of
// it into the initial bundle. Those bundles are now registered lazily
// (src/i18n/moduleStrings.ts) and consumed lazily (src/data/featureIndex.ts).
//
// But the always-loaded shell (sidebar, catalog grid, palette chrome, settings)
// needs a handful of namespaces BEFORE any lazy chunk loads. This script copies
// exactly those namespaces out of en.ts / zh-Hant.ts into a tiny eager file.
// Run after editing any of the SHELL namespaces below:
//   npm run gen:i18n-shell
// A vitest guard (src/i18n/shell.test.ts) fails if this file is stale.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const i18n = join(root, 'src', 'i18n');

// Namespaces the eager shell references via t('<ns>.…') outside src/modules.
// Keep in sync with the analysis in the PR: derived from t() first-segments used
// by non-module components (about/app/catalog/detail/nav/palette/reactor/status).
const SHELL_NS = ['about', 'app', 'catalog', 'detail', 'nav', 'palette', 'reactor', 'status'];

/**
 * Extract the verbatim `name: { … }` source spans for the requested top-level
 * namespaces from an i18n object-literal file. String/escape-aware brace matcher
 * so apostrophes, template literals and nested braces inside strings are ignored.
 */
function extractSpans(text, wanted) {
  const start = text.indexOf('{', text.indexOf('export const'));
  const found = new Map();
  let depth = 0;
  let inStr = null;
  let esc = false;
  let nameStart = -1;
  let curName = null;
  for (let i = start; i < text.length; i++) {
    const c = text[i];
    if (inStr) {
      if (esc) esc = false;
      else if (c === '\\') esc = true;
      else if (c === inStr) inStr = null;
      continue;
    }
    if (c === '"' || c === "'" || c === '`') { inStr = c; continue; }
    if (c === '{') {
      depth++;
      continue;
    }
    if (c === '}') {
      if (depth === 2 && curName) {
        if (wanted.includes(curName)) found.set(curName, text.slice(nameStart, i + 1));
        curName = null;
      }
      depth--;
      continue;
    }
    if (depth === 1 && !curName) {
      const m = /^([A-Za-z0-9_]+)\s*:\s*\{/.exec(text.slice(i, i + 80));
      if (m) {
        curName = m[1];
        nameStart = i;
        depth++;
        i += m[0].length - 1;
      }
    }
  }
  return found;
}

function build(file, wanted) {
  const text = readFileSync(join(i18n, file), 'utf8');
  const spans = extractSpans(text, wanted);
  const missing = wanted.filter((n) => !spans.has(n));
  if (missing.length) {
    throw new Error(`${file}: shell namespaces not found: ${missing.join(', ')}`);
  }
  // Emit in the canonical SHELL_NS order for stable output.
  return wanted.map((n) => `  ${spans.get(n)},`).join('\n');
}

const enBody = build('en.ts', SHELL_NS);
const yueBody = build('zh-Hant.ts', SHELL_NS);

const out = `// AUTO-GENERATED by tools/gen-i18n-shell.mjs — do not edit by hand.
// Eager subset of i18n strings: the namespaces the always-loaded shell needs
// before any lazy route chunk loads. The full per-module bundles live in
// en.ts / zh-Hant.ts / batchB.ts and are registered lazily (moduleStrings.ts).
// Regenerate with: npm run gen:i18n-shell
export const enShell = {
${enBody}
};

export const yueShell = {
${yueBody}
};
`;

writeFileSync(join(i18n, 'shell.ts'), out);
console.log(`shell.ts: ${SHELL_NS.length} namespaces (${SHELL_NS.join(', ')})`);
