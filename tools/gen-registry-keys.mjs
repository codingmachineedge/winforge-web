// Regenerates src/modules/registryKeys.ts — the value-free list of registered
// module tags. status.ts needs only the KEYS of moduleRegistry; importing the
// registry itself drags all module components into the eager bundle and defeats
// route-level code splitting. Run after adding/removing registry entries:
//   npm run gen:registry-keys
// A vitest guard (src/modules/registryKeys.test.ts) fails if this file is stale.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const sources = ['registry.tsx', 'registryA.tsx', 'registryB.tsx'];

const tags = new Set();
for (const file of sources) {
  const text = readFileSync(join(root, 'src', 'modules', file), 'utf8');
  // 'dashboard' is the one catalog tag without the module. prefix.
  for (const m of text.matchAll(/'(module\.[a-z0-9_-]+|dashboard)'\s*:/gi)) tags.add(m[1]);
}

// The Windows-tweak categories are registered dynamically (registryTweaks.tsx spreads
// moduleRegistryTweaks, whose keys are built as `module.tweaks.<catId>` from tweaks.ts),
// so there are no literal string keys for the scan above to find. Mirror them here.
const tweaksText = readFileSync(join(root, 'src', 'data', 'tweaks.ts'), 'utf8');
const tcMarker = 'export const tweakCategories: TweakCategory[] =';
const tcStart = tweaksText.indexOf('[', tweaksText.indexOf(tcMarker) + tcMarker.length);
const tcEnd = tweaksText.indexOf('\n];', tcStart);
if (tcStart >= 0 && tcEnd >= 0) {
  for (const c of JSON.parse(tweaksText.slice(tcStart, tcEnd + 2))) tags.add(`module.tweaks.${c.id}`);
}

const sorted = [...tags].sort();
const body = `// AUTO-GENERATED by tools/gen-registry-keys.mjs — do not edit by hand.
// Value-free mirror of the moduleRegistry keys so status checks stay out of the
// eager bundle. Regenerate with: npm run gen:registry-keys
export const registeredModuleTags: ReadonlySet<string> = new Set([
${sorted.map((t) => `  '${t}',`).join('\n')}
]);
`;

writeFileSync(join(root, 'src', 'modules', 'registryKeys.ts'), body);
console.log(`registryKeys.ts: ${sorted.length} tags from ${sources.join(', ')}`);
